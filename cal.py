
num1 = float(input("Enter no.1: "))
op = (input("Enter +, -, *, /: "))
num2 = float(input("Enter no.2: "))

if op == "+":
    print(num1 + num2)

elif op == "-":
    print(num1 - num2)

elif op == "*":
      print(num1 * num2) 

elif op == "/":
     print(num1 / num2)  

else:
    print("Invalid Operator!")
